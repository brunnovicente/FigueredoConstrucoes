    public void atualizaTabela(){
        List<Usuario> lista = Banco.getBanco().consultaUsuario();
        DefaultTableModel modelo = (DefaultTableModel) jtabela.getModel();
        int tam = modelo.getRowCount();
        for(int i=0;i<tam;i++){
            modelo.removeRow(0);
        }
        for(Usuario u : lista){
            String[] linha = new String[3];
            linha[0] = u.getNome();
            linha[1] = u.getLogin();
            linha[2] = u.getEmail()+"";
            modelo.addRow(linha);
        }
    }
    
